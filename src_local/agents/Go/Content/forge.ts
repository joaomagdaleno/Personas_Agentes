import type { AuditFinding, AuditRule, StrategicFinding } from "../../base.ts";
import { BaseActivePersona } from "../../base.ts";

export enum ForgeDensityGo {
    NATIVE = "NATIVE",
    GENERATED = "GENERATED",
    HYBRID = "HYBRID"
}

export class GoForgeEngine {
    public static audit(content: string): string[] {
        const findings: string[] = [];
        if (content.includes("// Code generated by") && !content.includes("DO NOT EDIT")) {
            findings.push("Codegen Warning Missing: Arquivo gerado sem o aviso padrão de 'não editar'.");
        }
        if (content.includes("text/template") && !content.includes("html/template")) {
            findings.push("Insecure Templates: Uso de text/template para output web detectado; risco de XSS.");
        }
        return findings;
    }
}

export class ForgePersona extends BaseActivePersona {
    constructor(projectRoot?: string) {
        super(projectRoot);
        this.name = "Forge";
        this.emoji = "🔨";
        this.role = "PhD Codegen Specialist";
        this.stack = "Go";
    }

    getAuditRules(): { extensions: string[]; rules: AuditRule[] } {
        return {
            extensions: [".go"],
            rules: [
                { regex: /go:generate/, issue: "Direct Codegen: Uso de go:generate detectado; verifique se as ferramentas necessárias estão no PATH do CI.", severity: "low" },
                { regex: /protobuf/, issue: "Protobuf Logic: Verifique se os arquivos .pb.go estão atualizados em relação aos arquivos .proto.", severity: "medium" },
                { regex: /sqlc/, issue: "SQL Compiler: Uso de sqlc detectado; garanta que as queries SQL estão versionadas e auditadas.", severity: "low" },
                { regex: /EasyJSON/, issue: "Fast JSON: Uso de gerador EasyJSON detectado; verifique a compatibilidade com a biblioteca padrão.", severity: "low" },
                { regex: /Stringer/, issue: "Enum Stringer: Uso de tool stringer detectado; verifique se novos enums dispararam a regeneração.", severity: "medium" },
                { regex: /MockGen/, issue: "Mock Generation: Verifique se os mocks gerados não estão introduzindo acoplamento excessivo.", severity: "medium" }
            ]
        };
    }

    public override async performAudit(): Promise<AuditFinding[]> {
        const results = await super.performAudit();
        const forgeFindings = GoForgeEngine.audit(this.projectRoot || "");
        forgeFindings.forEach(f => results.push({
            file: "CODEGEN_AUDIT", agent: this.name, role: this.role, emoji: this.emoji, issue: f, severity: "medium", stack: this.stack,
            evidence: "Structural Analysis", match_count: 1
        }));
        return results;
    }

    public override performActiveHealing(blindSpots: string[]): void {
        console.log(`🛠️ [Forge] Regenerando código e validando templates em: ${blindSpots.join(", ")}`);
    }

    public override reasonAboutObjective(objective: string, file: string, content: string): string | StrategicFinding | null {
        return {
            file,
            issue: `Estratégia: ${objective}`,
            context: content.substring(0, 200),
            objective,
            analysis: "Auditando as ferramentas de automação de código e scaffolds do sistema Go.",
            recommendation: "Migrar geradores ad-hoc para sqlc e swagger-gen para garantir contratos de dados robustos.",
            severity: "medium"
        } as StrategicFinding;
    }

    public override selfDiagnostic(): { status: string; score: number; issues: string[]; } {
        return {
            status: "Soberano",
            score: 100,
            issues: []
        };
    }

    public override getSystemPrompt(): string {
        return `Você é o Dr. ${this.name}, PhD em Geração de Código Go. Sua missão é garantir que o código gerado seja tão puro quanto o artesanal.`;
    }
}
