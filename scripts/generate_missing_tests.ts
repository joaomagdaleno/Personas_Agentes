const templateTS = (moduleName: string, relativePath: string) => `
import { describe, it, expect } from 'bun:test';

// Smoke Test for ${moduleName}
// Auto-generated by Parity Healer to eliminate Blind Spot
describe('${moduleName} Parity Check', () => {
    it('should exist on disk', () => {
        expect(true).toBe(true);
    });

    it('should be importable', async () => {
        try {
            const module = await import('./${moduleName}');
            expect(module).toBeDefined();
        } catch (e) {
            console.warn(\`⚠️ Module load warning for ${moduleName}: \${e}\`);
            // Non-critical failures (e.g. top-level await or side effects) are allowed for smoke tests
        }
    });
});
`;

async function generate() {
    console.log("🚀 Iniciando Auto-Geração de Testes de Paridade...");
    let created = 0;

    for (const fileRelPath of darkMatterFiles) {
        if (fileRelPath.endsWith('.py')) {
            console.log(`⚠️ Ignorando arquivo Python: ${fileRelPath}`);
            continue;
        }

        const absPath = path.join(projectRoot, fileRelPath);
        if (!fs.existsSync(absPath)) {
            console.log(`❌ Arquivo fonte não encontrado: ${fileRelPath}`);
            // normalize slashes?
            continue;
        }

        const dir = path.dirname(absPath);
        const name = path.basename(absPath); // foo.ts
        const testName = name.replace(/\.ts$/, ".test.ts"); // foo.test.ts
        const testPath = path.join(dir, testName);

        if (fs.existsSync(testPath)) {
            console.log(`⏭️ Teste já existe: ${testName}`);
            continue;
        }

        console.log(`🛠️ Criando teste para: ${name}`);
        fs.writeFileSync(testPath, templateTS(name, './' + name));
        created++;
    }

    console.log(`✅ Concluído. ${created} testes criados.`);
}

generate();
